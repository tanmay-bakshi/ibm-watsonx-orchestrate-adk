from typing import Optional, List, Any, Dict
from pydantic import BaseModel, Field


class AgentInteractionResult(BaseModel):
    """
    Structured result from agent interaction containing all messages and traces.
    Used for programmatic access (MCP, API).
    """
    status: str = Field(
        description="The success status of the request. Either 'success' or 'error'"
    )
    final_message: Optional[str] = Field(
        default=None,
        description="The final response generated by the agent for the user's query"
    )
    thinking_trace: List[str] = Field(
        default_factory=list,
        description="List of intermediate thinking/progress messages captured during agent execution"
    )
    reasoning_trace: Optional[Dict[str, Any]] = Field(
        default=None,
        description="Agent's reasoning steps and decision-making process if requested via include_reasoning flag"
    )
    thread_id: Optional[str] = Field(
        default=None,
        description="Thread ID for conversation continuity. Use this to continue the conversation in subsequent calls"
    )
    error: Optional[str] = Field(
        default=None,
        description="Error message if status is 'error', None otherwise"
    )

